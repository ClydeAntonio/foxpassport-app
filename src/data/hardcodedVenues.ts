// --- Helper Functions ---
const generateId = (location: string, index: number): string => {
  return `${location.toLowerCase().replace(/[^a-z0-9]/g, "-")}-${index}`;
};

const getRandomRating = (): number => {
  return Number((4.5 + Math.random() * 0.5).toFixed(2));
};

const getRandomReviews = (): number => {
  return Math.floor(20 + Math.random() * 300);
};

const getRandomPrice = (range: [number, number]): number => {
  return Math.floor(range[0] + Math.random() * (range[1] - range[0]));
};

const getRandomInRange = (range: [number, number]): number => {
  return Math.floor(range[0] + Math.random() * (range[1] - range[0] + 1));
};

const getRandomHost = (index: number): Venue["host"] => {
  const name = HOST_NAMES[index % HOST_NAMES.length];
  const year = 2015 + Math.floor(Math.random() * 9);
  const yearsHosting = 2024 - year;
  const isCertifiedFoxer = Math.random() > 0.4;

  // Dummy Co-hosts
  const coHostName = HOST_NAMES[(index + 5) % HOST_NAMES.length];
  const coHostAvatar = `https://i.pravatar.cc/150?u=${coHostName.toLowerCase()}${
    index + 5
  }`;

  return {
    name,
    avatar: `https://i.pravatar.cc/150?u=${name.toLowerCase()}${index}`,
    isCertifiedFoxer,
    joined: `Joined ${year}`,
    description: `Hi! I'm ${name}. I love hosting travelers in our beautiful city. I've been running this guesthouse for ${yearsHosting} years and I make sure every guest feels at home.`,
    responseRate: 100,
    responseTime: "within an hour",
    coHosts:
      Math.random() > 0.7 ? [{ name: coHostName, avatar: coHostAvatar }] : [],
    work: "Hospitality Manager",
    funFact: "I've traveled to over 30 provinces in the Philippines!",
    details: [`Response rate: 100%`, `Responds within an hour`],
    reviewCount: Math.floor(50 + Math.random() * 500),
    rating: Number((4.5 + Math.random() * 0.5).toFixed(2)),
    yearsHosting,
  };
};

